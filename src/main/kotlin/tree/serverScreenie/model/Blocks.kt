    fun toTexture(): Array<IntArray> {
        val textures = loadTextures()

        val texture = Array(16) { IntArray(16) }
        val offsetY = row * 16
        val offsetX = col * 16

        for (pixelY in 0 until 16) {
            for (pixelX in 0 until 16) {
                texture[pixelY][pixelX] = textures.getRGB(offsetX + pixelX, offsetY + (15 - pixelY))
            }
        }

        return texture
    }

    private fun loadTextures(): BufferedImage {
        val url = ServerScreenie.instance?.getResource("terrain.png")
        if (url == null) {
            throw RuntimeException("Missing texture file: terrain.png")
        }

        return ImageIO.read(url)
    }

